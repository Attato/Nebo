import { useState } from 'react';

import type { NextPage } from 'next';
import Head from 'next/head';
import Link from 'next/link';
import Image from 'next/image';

import Header from 'components/header/header';
import Footer from 'components/footer/footer';

import { motion } from 'framer-motion';

import { usePopularMovies } from 'hooks/movies/usePopularMovies';
import { useFilterList } from 'hooks/movies/useFilterList';

import styles from './movies.module.scss';

const Movies: NextPage = () => {
	// Запрос популярных фильмов
	const { popularMovies } = usePopularMovies();

	const [search, setSearch] = useState('');

	// Фильтры
	const {
		filterList,
		isFilterListOpen,
		setIsFilterListOpen,
		currentfilterItem,
		setCurrentFilterItem,
	} = useFilterList();

	return (
		<div className="container">
			<Head>
				<title>Фильмы</title>
				<meta name="description" content="Generated by create next app" />
				<link rel="icon" href="/favicon.ico" />
			</Head>

			<Header />

			<main className={styles.main}>
				<div className={styles.page__wrap}>
					<div className={styles.search}>
						<h4>Шаблоны фильтров</h4>
						<div className={styles.input}>
							<Image
								src="/svg/search.svg"
								width={16}
								height={16}
								alt="svg"
							></Image>
							<input type="text" placeholder="Поиск..." />
						</div>
						<div className={styles.filter}>
							{filterList.map((item, id) => {
								return (
									<div className={styles.button__wrap} key={id}>
										<button
											onClick={() => {
												setIsFilterListOpen(!isFilterListOpen);
												currentfilterItem === id
													? setCurrentFilterItem(null)
													: setCurrentFilterItem(id);
											}}
										>
											{currentfilterItem === id ? (
												<Image
													src="/svg/arrow.svg"
													width={16}
													height={16}
													alt="svg"
													style={{ transform: 'rotate(90deg)' }}
												></Image>
											) : (
												<Image
													src="/svg/arrow.svg"
													width={16}
													height={16}
													alt="svg"
												></Image>
											)}
											{item.name}
										</button>

										<div
											className={
												currentfilterItem === id
													? styles.subitems__active
													: styles.subitems
											}
										>
											{item.subitems.map((subitem, id) => {
												return (
													<label key={id}>
														<span className={styles.checkbox}>
															<input type="checkbox" id={subitem.name} />

															<Image
																src="/svg/check/check.svg"
																width={16}
																height={16}
																alt="svg"
															></Image>
														</span>
														{subitem.name}
													</label>
												);
											})}
										</div>
									</div>
								);
							})}
						</div>
					</div>
					<div className={styles.cards__wrap}>
						{popularMovies.map((movie) => {
							return (
								<motion.div
									className={styles.card}
									key={movie.id}
									layout
									animate={{ opacity: 1 }}
									initial={{ opacity: 0 }}
									exit={{ opacity: 0 }}
								>
									<Link href={`/movies/${movie.slug}`}>
										<img
											src={`https://image.tmdb.org/t/p/w500${movie.backdrop_path}`}
											width={220}
											height={180}
											alt="image"
											draggable="false"
										/>
										<h3>
											{movie.vote_average.toFixed(1)} &nbsp; {movie.title}
										</h3>
										<p>{movie.overview}</p>
									</Link>
								</motion.div>
							);
						})}
					</div>
				</div>
			</main>

			<Footer />
		</div>
	);
};

export default Movies;
